"""数値を2倍にして出力するプログラム。

リストのミュータブル（変更可能）な特性を利用し、
C言語のポインタ（参照渡し）のような動きをPythonで再現。
ユーザーから入力を受け取り、関数内でリストの要素を直接変更して結果を出力。

Author:
    kusakabe
Date:
    2026-04-13
"""

def double_value(ans_list):
    """リストの先頭要素の数値を2倍にする関数。

    Pythonでは整数型はイミュータブル（変更不可）であるため、そのままでは関数内で書き換えができない。
    この関数はミュータブルな「リスト」を引数として受け取り、その0番目の要素を直接2倍に書き換えることで、
    戻り値（return）を使わずに呼び出し元のデータを更新する。

    Args:
        ans_list (list): 処理対象の数値が格納されたリスト。最低1つの要素（数値）が必要。
    """
    ans_list[0] *= 2

# スクリプトとして直接実行された場合のみ以下の処理を行う
if __name__ == "__main__":
    # ユーザーから入力された元の数値（整数）
    inval = int(input("数値を入力："))
    
    # 参照渡しのように関数へ渡すために、数値を格納したリスト
    data_list = [inval]
    
    # 関数を呼び出して data_list の中身を直接書き換える
    double_value(data_list)
    
    # 結果の出力
    print(f"result={data_list[0]}")